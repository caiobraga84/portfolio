document.getElementById('caio').addEventListener('click', function() {
    const caio = document.querySelector('#caio');
    
    if (caio.style.top === '-5px') {
        caio.style.top = '-150px';
    } else {
        caio.style.top = '-5px';
        
    }
    
});

